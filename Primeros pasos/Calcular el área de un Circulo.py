#Ejercicio Número 3 Obtener radio y logitud de un circulo en python
import math

#Leemos la medida del radio de la circunferencia.
radio = float(input("Ingresa el radio del circulo: "))

#Aplicamos la Formula matemática del área con operadores aritmeticos.
area = math.pi*radio**2

#Aplicamos la Fórmula matemática del área con operadores aritmeticos.
perimetro = 2*math.pi*radio

#Imprimimos los resultados.
print("El area del circulo es: ",area)
print("El perímetro del circulo es: ",perimetro)
