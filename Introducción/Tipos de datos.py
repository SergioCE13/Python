#Tipos de Datos principales en Python

entero = 50 #Número entero
decimal = 13.22 #Número decimal
boleano = 1 #Valor boleano
cadena = "Hola mundo" #Cadena de caracteres

#Impresión de Datos en Python

print("El número entero es: ", entero)
print("El número decimal: ", decimal)
print("El valor boleano es: ", boleano)
print("La caden es: ", cadena)

