#Entrada de datos en Python

print("Ingreso de datos de tipo String:") 
cad1 = input("Ingrese una cadena: ") #La función input recibe una cadena de texto y espera la respuesta desde teclado.
cad2 = input("Ingrese otra cadena: ")

print("Ingreso de datos de tipo Entero: ")
num1 = int(input("Ingrese un número: ")) #Se realiza un cast para señalar que el programa debe recibir un número entero.

#Salida de datos en Python

print(f"La cadena completa sería {cad1} {cad2}") #Se mandan a imprimir las dos cadenas leidas inicialmente.

print(f"El número ingresado es: {num1}") #Se manda a imprimir el valor númerico casteado a entero.