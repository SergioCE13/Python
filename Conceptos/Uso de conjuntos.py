# Ejercicio número 10: Conjuntos
A = {1,2,3,4}
B = {2,3,5,6}
C = {3,4,6,7}

#Igualdad entre conjuntos
print(A==B)
print(A==C)

#Unión entre conjuntos
print(A|B)
print(A|C)

#Intersection entre conjuntos
print(A&B)
print(A&C)

#Resta entre conjuntos
print(A-B)
print(A-C)

#Diferencia simetrica entre conjuntos (unión menos intersección)
print(A^B)
print(A^C)
